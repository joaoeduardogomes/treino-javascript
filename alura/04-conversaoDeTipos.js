console.log("Conversão de Tipos")

console.log("2" + "2"); //ele cola os dois
console.log(parseInt("2") + parseInt("2")); //ele soma os dois, pois o parseInt converte para um número inteiro se for possível

console.log("10" / "2"); //Neste caso ele faz a divisão, pois a barra só serve para dividir, diferente da soma, que serve também para concatenar.

console.log("Caio" / "2"); //Ele retorna "NaN" (Not a Number)